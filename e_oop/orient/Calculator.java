package e_oop.orient;


public class Calculator {

	//메서드 5개 연산자별 하나씩 
	
	double plus(double a, double b){
		return a+b;	
	}
	double minus(double a, double b){
		return a-b;
		
	}
	double multi(double a, double b){
		return a*b;
	}
	double divid(double a, double b){
		return a/b;
	}
	double rest(double a, double b){
		return a%b;
	}
	
	
}
